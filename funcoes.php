<?php

function soma($num1, $num2){
    return($num1 + $num2);
}
function subtrair($num1, $num2){
    return($num1 - $num2);
}
function divisao($num1, $num2){
    return($num1 / $num2);
}
function multiplicar($num1, $num2){
    return($num1 * $num2);
}
function raizQuadrada($num1){
    return($num1^(1/2));
}

?>